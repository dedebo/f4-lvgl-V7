//设置LCD自动扫描方式
void LCD_Scan_Dir(u8 dir)
{
	if(dir == 1)//竖屏的时候
	{
		LCD_WriteReg(0x36,0x00); //从左到右，从上到下
	}
	else if(dir == 0)//横屏的时候
	{
		LCD_WriteReg(0x36,0x60);//从上到下，从右到左
	}
	
	LCD_WR_REG(lcddev.setxcmd); 
	LCD_WR_DATA(0);LCD_WR_DATA(0);
	LCD_WR_DATA((lcddev.width-1)>>8);LCD_WR_DATA((lcddev.width-1)&0XFF);
	LCD_WR_REG(lcddev.setycmd); 
	LCD_WR_DATA(0);LCD_WR_DATA(0);
	LCD_WR_DATA((lcddev.height-1)>>8);LCD_WR_DATA((lcddev.height-1)&0XFF);  
	
}

//竖屏1，横屏0
void LCD_Display_Dir(u8 dir)
{
	if(dir)//竖屏
	{
		;//什么都不用做
	}
	else if(dir == 0)//横屏
	{
		//改变长宽
		lcddev.width = 480;
		lcddev.height = 320;
		lcddev.dir = 0;//表示横屏显示
		//为何不改变 写x命令和写y命令的原因是 将采用改变扫描方式的方法
	}
	LCD_Scan_Dir(dir);//设置自动扫描方式
}

//清屏函数
