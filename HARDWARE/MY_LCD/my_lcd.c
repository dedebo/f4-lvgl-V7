//打印一个汉字字符
//num:为字库中的横坐标
//size：大小
//mode：是否采用叠加模式 1：采用叠加模式 0：不采用叠加模式
void LCD_ShowChineseChar(u16 x,u16 y,u8 num,u8 size, u8 mode)
{
	u8 temp,t,t1;
	u16 y0 = y;
	u8 csize = (size/8+(size%8?1:0))*size;
	for(t1=0;t1<csize;t1++)
	{
		temp = LinXingYu[num][t1];       //LinXingYu 为已提取的字库，根据自己需要提取
		for(t=0;t<8;t++)
		{
			if(temp&0x80)LCD_Fast_DrawPoint(x,y,POINT_COLOR);
			else if(mode==0)LCD_Fast_DrawPoint(x,y,BACK_COLOR);
			temp <<=1;
			y++;
			if(y-y0==size)
			{
				y=y0;
				x++;
				if(x>lcddev.width)return;
				break;
			}
		}
	}
}

//打印一串汉字
//count 为该汉字在自己字库中的横坐标
void LCD_ShowChineseString(u16 x,u16 y,u16 width,u16 height,u8 size,u8 *p)
{
	u16 x0 = x;
	u8 count=0;
	
	width+=x;
	height+=y;
	while(*p!='\0')
	{
		p++;
		LCD_ShowChineseChar(x,y,count,size,0);
		p+=1;
		count+=1;
		x+=size;
		if(x>=width)
		{
			x=x0;
			y+=size;
		}
	}
}



//在指定区域内填充指定颜色块			 
//(sx,sy),(ex,ey):填充矩形对角坐标,区域大小为:(ex-sx+1)*(ey-sy+1)   
//color:要填充的颜色
void LCD_Color_Fill(u16 sx,u16 sy,u16 ex,u16 ey,u16 *color)
{  
	u16 height,width;
	u16 i,j;
	width=ex-sx+1; 			//得到填充的宽度
	height=ey-sy+1;			//高度
 	for(i=0;i<height;i++)
	{
 		LCD_SetCursor(sx,sy+i);   	//设置光标位置 
		LCD_WriteRAM_Prepare();     //开始写入GRAM
		for(j=0;j<width;j++)LCD->LCD_RAM=color[i*width+j];//写入数据 
	}		  
}
